package Project;

public class User {

	
		int userId;
		String Name;
		public User(int userId, String name) {
			super();
			this.userId = userId;
			Name = name;
		}
		public int getUserId() {
			return userId;
		}
		public void setUserId(int userId) {
			this.userId = userId;
		}
		public String getName() {
			return Name;
		}
		public void setName(String name) {
			Name = name;
		}
		
			

	}


